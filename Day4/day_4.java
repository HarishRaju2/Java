public class day_4
{
    public static void main(String args[])
    {
        int x=0;
        while(x<10)
        {
            if(x%2==0)
            {
                System.out.println("Even number");
            }
            else 
            {
                System.out.println("Odd");
            }
            x++;
        }
    }
}